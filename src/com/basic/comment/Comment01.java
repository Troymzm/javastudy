package com.basic.comment;

// 演示注释使用
public class Comment01 {
    // 编写一个main方法
    public static void main(String[] args) {
        /*
         * 示意 -- 多行注释
         * 下面代码完成 两个数相加
         * 定义变量
         */
        int n1 = 10;
        int n2 = 30;
        // 求和
        int sum = n1 + n2;
        // 输出结果
        System.out.println("结果=" + sum);
    }
}
